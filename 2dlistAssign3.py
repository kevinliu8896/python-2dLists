def check_distance(yard, x, y):  # pythagorean theorem to check distance between trees in the backyard
    for k in range(30):
        for l in range(30):
            if yard[k][l] == 1:
                d = math.sqrt((k - x) ** 2 + (l - y) ** 2)
                if d <= 3:
                    return True
    return False


